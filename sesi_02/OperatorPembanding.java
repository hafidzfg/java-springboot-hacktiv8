public class OperatorPembanding {
    public static void main(String[] args) {
        int nilaiA = 12, nilaiB = 4;
        boolean hasil;

        // a > b?
        hasil = nilaiA > nilaiB;
        System.out.println(hasil);

        // a < b?
        hasil = nilaiA < nilaiB;
        System.out.println(hasil);

        // a >= b?
        hasil = nilaiA >= nilaiB;
        System.out.println(hasil);

        // a <= b?
        hasil = nilaiA <= nilaiB;
        System.out.println(hasil);

        // a sama dengan b?
        hasil = nilaiA == nilaiB;
        System.out.println(hasil);

        // a tidak sama dengan b?
        hasil = nilaiA != nilaiB;
        System.out.println(hasil);
    }

}
